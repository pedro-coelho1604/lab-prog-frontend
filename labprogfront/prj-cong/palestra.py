class Palestra:
    def __init__(self, id, nome, descricao, dia, mes, palestrantes, foto, mapa):
        self.id = id
        self.nome = nome
        self.descricao = descricao
        self.dia = dia
        self.mes = mes
        self.palestrantes = palestrantes
        self.foto = foto
        self.mapa = mapa